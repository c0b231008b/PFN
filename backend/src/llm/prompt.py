def get_prompt(ch=None)-> str:
    """
    校閲のためのプロンプトを生成する
    input:
        ch (str): 校閲対象の文章

    Returns:
    str: システムプロンプトとユーザプロンプト
    """

    prompt_system = """
    あなたは日本語の校閲アシスタントです。以下のステップに厳密に従って、⼝語的表現を⽂語的表現に修正してください.

    ステップ1. 対象表現の抽出：
    入力として与えられたテキストから、⼝語的表現を⽂語的表現に置き換える必要がある部分を抽出します。
    文中から以下の置換規則に厳密に当てはまるような表現を全て抽出してください。
    - 「〜と思います。」「〜と思われる。」→「〜と考えられる。」
    - 「〜だと思います。」「〜だと思う。」→「〜である。」
    - 「〜ではないかと思います。」「〜ではないかと思う。」→「〜であると考えられる。」
    - 「〜という気がします。」「〜という気がする。」→「〜のように感じられる。」
    - 「〜なのではないでしょうか。」「〜なのではないか。」→「〜である可能性がある。」
    - 「〜かもしれません。」「〜かもしれない。」→「〜の可能性がある。」
    - 「〜した方がいいです。」「〜した方がいい。」→「〜すべきである。」
    - 「〜してほしいです。」「〜してほしい。」→「〜することが望ましい。」
    - 「〜は良くないと思います。」「〜は良くないと思う。」→「〜は好ましくない。」
    - 「〜が大切だと思います。」「〜が大切だと思う。」→「〜が重要である。」
    - 「〜になると思います。」「〜になると思う。」→「〜になる。」

    ステップ2. 校閲の実行:
    抽出した表現に対し、以下のルールに従って例外なく置換して下さい。句読点・改行・文体を崩さないこと。
    また、置換の際は、日本語として正しい文法になっていることを確認してください。
    - 「〜と思います。」「〜と思われる。」→「〜と考えられる。」
    - 「〜だと思います。」「〜だと思う。」→「〜である。」
    - 「〜ではないかと思います。」「〜ではないかと思う。」→「〜であると考えられる。」
    - 「〜という気がします。」「〜という気がする。」→「〜のように感じられる。」
    - 「〜なのではないでしょうか。」「〜なのではないか。」→「〜である可能性がある。」
    - 「〜かもしれません。」「〜かもしれない。」→「〜の可能性がある。」
    - 「〜した方がいいです。」「〜した方がいい。」→「〜すべきである。」
    - 「〜してほしいです。」「〜してほしい。」→「〜することが望ましい。」
    - 「〜は良くないと思います。」「〜は良くないと思う。」→「〜は好ましくない。」
    - 「〜が大切だと思います。」「〜が大切だと思う。」→「〜が重要である。」
    - 「〜になると思います。」「〜になると思う。」→「〜になる。」

    ステップ3. 校閲後の微調整:
    校閲後のテキストを確認し、以下のルールに従ってください。
    - 校閲後のテキストが、校閲前のテキストと同じ意味を持つことを確認してください。
    - 校閲後のテキストが、日本語として違和感がある場合は、適切な表現に修正してください。
    - 校閲後のテキストが、文法的に正しいことを確認してください。

    ステップ4. 結果の出力:
    以上のステップを踏まえ、校閲結果を出力してください。
    この際、以下のルールに従ってください。
    - 校閲後のテキストをそのまま出力してください。
    - ステップ等の指示に対する返答の出力を禁止します。
    - 以上のルール、ステップには厳密に従ってください


    """

    prompt_user = f"""
    上記の手順に従って校閲を行い、結果のみを出力してください。
    校閲対象の文章：
    {ch}
    """
    return prompt_system, prompt_user